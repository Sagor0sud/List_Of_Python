# Accessing Value From List
# by (1) Indexing
My_List = ['Mehedi', 'Sagor', 'Hasan', 'Sadia', 'Sopna']
print(My_List[0])
# output is Mehedi

print(My_List[1:5])
# Output is
# ['Sagor', 'Hasan', 'Sadia', 'Sopna']

# Accessing Value From List
# by (2) Negative Indexing

print(My_List[-5:-2])
# output is
#  ['Mehedi', 'Sagor', 'Hasan']

# Accessing Value From List
# by (3) Slicing

print(My_List[:-3])
# Output is
# ['Mehedi', 'Sagor']
print(My_List[:4])
# Output is
# ['Mehedi', 'Sagor', 'Hasan', 'Sadia']
